int		ft_isspace(int c)
{
	unsigned char ch;

	ch = (unsigned char)c;
	if (ch == '\t' || ch == '\v' || ch == '\f' || ch == '\n' || ch == '\r'
			|| ch == ' ')
		return (1);
	else
		return (0);
}
